                  <h1>Katherine Gili</h1>

                  <p>b.1948, Oxford<br> Lives and works in Kent, England</p>

                  <p>Katherine Gili graduated
                    from Bath Academy of Art in 1970 then spent a year juggling earning a living with making sculpture
                    in
                    London.
                    Having written to and then met with Anthony Caro she went on to study at St Martin’s School of Art
                    on
                    his
                    recommendation. In 1973 she set up a studio at Stockwell Depot alongside a group of like-minded
                    artists. The group
                    established a reputation with a series of annual exhibitions throughout the decade, during which
                    Gili’s work
                    received considerable attention. So much so that sculptures from this period were purchased by the
                    Arts Council at
                    the time and later by the Tate. This also led to a solo exhibition in New York with several of
                    Gili’s
                    sculptures
                    from this period now in private collections in the USA. In 1978 she moved to the Greenwich Studios
                    and
                    began to
                    radically develop her work, which in turn led to exhibitions at Tate Gallery in 1984 and the Conde
                    Duque Centre in
                    Madrid in1988.</p>

                  <p>
                    Katherine Gili’s sculpture has been exhibited in both solo and group shows well over a
                    hundred times and her career is marked by contributions to seminal survey exhibitions at major
                    venues
                    such as the
                    Hayward Gallery (three times) and the Henry Moore Institute. She has regularly shown in the Royal
                    Academy Summer
                    Exhibitions, in 2013 her sculpture “Ripoll” won the Sculpture Prize.
                  </p>

                  <p>Gili taught part-time at
                    several art schools, most notably St Martins and Norwich and served as a selector for exhibitions
                    such
                    as “New
                    Contemporaries 1978”, “Serpentine Summer Show 1979” and “Have You Seen Sculpture from the Body?” at
                    Tate Gallery
                    in 1984. She scaled back these commitments on moving to Kent in 1995.</p>

                  <p>Gili’s work is represented
                    in public and corporate collections in the UK, Switzerland, and the USA. Lord Foster selected one of
                    her pieces to
                    stand alongside the Cranfield University Institute of Technology Library in 1992. Her sculptures can
                    also be found
                    in many private collections in Britain, Spain, and the USA.</p>

